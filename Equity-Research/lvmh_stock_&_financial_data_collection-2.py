# Drop rows where 'Date' is NaT before plotting
geopolitical_events = geopolitical_events.dropna(subset=['Date'])

# Ensure stock_data is available (it's loaded in a previous cell t5FpOKei6cPV)

plt.figure(figsize=(16, 9)) # Increased figure size for better readability
ax = sns.lineplot(x=stock_data.index, y=stock_data['MC.PA'], label='LVMH Stock Price', color='blue', linewidth=1.5)

# Set aesthetic improvements
plt.title("LVMH Stock Price with Geopolitical Events (2019-2022)", fontsize=16, fontweight='bold')
plt.xlabel("Date", fontsize=12)
plt.ylabel("Stock Price (EUR)", fontsize=12)
plt.legend(title="Company", loc='upper left', fontsize=10, title_fontsize=12, frameon=True, shadow=True)
plt.grid(True, linestyle='--', alpha=0.7)
plt.xticks(rotation=45, ha='right')

# Get current y-axis limits to place annotations below the plot area
min_y, max_y = ax.get_ylim()

# Define a window to check for stock downturn/upturn after an event (e.g., 5 trading days)
downturn_window_days = 5
downturn_threshold_percent = -0.02 # e.g., a 2% drop is considered a downturn
upturn_threshold_percent = 0.02 # e.g., a 2% rise is considered an upturn

# Iterate through events for annotations and downturn analysis
# Sort events by date to potentially help with annotation placement, though true non-overlap requires more complex logic
geopolitical_events = geopolitical_events.sort_values(by='Date').reset_index(drop=True)

# Determine a base y-position for annotations, and then stagger them
base_annotation_y = min_y - (max_y - min_y) * 0.20 # Place annotations 20% of the y-range below min_y
annotation_y_step = (max_y - min_y) * 0.03 # Small step to stagger text

for i, event in geopolitical_events.iterrows():
    event_date = event['Date']
    event_label = event['Event']

    # Find the nearest trading day for the event and price on that day
    if event_date in stock_data.index:
        price_on_event = stock_data.loc[event_date, 'MC.PA']
        event_trading_day_idx = stock_data.index.get_loc(event_date)
    else:
        # If event_date is not a trading day, find the closest previous trading day
        nearest_trading_days = stock_data.index[stock_data.index <= event_date]
        if not nearest_trading_days.empty:
            event_date = nearest_trading_days[-1] # Use the closest previous trading day
            price_on_event = stock_data.loc[event_date, 'MC.PA']
            event_trading_day_idx = stock_data.index.get_loc(event_date)
        else:
            continue # Skip event if no suitable trading day found

    # Find the stock price after the downturn_window_days (trading days)
    price_after_event = None
    price_change_percent = 0
    if event_trading_day_idx + downturn_window_days < len(stock_data):
        price_after_event = stock_data['MC.PA'].iloc[event_trading_day_idx + downturn_window_days]
        price_change_percent = (price_after_event - price_on_event) / price_on_event

    # Determine annotation color and text based on downturn/upturn
    line_color = 'gray' # Default color
    arrow_color = 'gray'
    annotation_suffix = ''

    if price_change_percent < downturn_threshold_percent:
        line_color = 'red'
        arrow_color = 'red'
        annotation_suffix = f" (↓ {price_change_percent:.1%})"
    elif price_change_percent > upturn_threshold_percent:
        line_color = 'green'
        arrow_color = 'green'
        annotation_suffix = f" (↑ {price_change_percent:.1%})"

    # Draw the vertical line for the event
    ax.axvline(x=event_date, color=line_color, linestyle='--', alpha=0.7, lw=1)

    # Add text annotation using annotate, staggered for better readability
    text_y_pos = base_annotation_y + (i % 3) * annotation_y_step # Stagger annotations vertically

    ax.annotate(event_label + annotation_suffix,
                xy=(event_date, price_on_event), # Pointing to the event on the stock price line
                xytext=(event_date, text_y_pos), # Place text below the plot area
                textcoords='data',
                arrowprops=dict(arrowstyle="->", color=arrow_color, shrinkA=5, shrinkB=5, lw=0.8),
                ha='center', # Horizontal alignment for text
                rotation=45, # Rotate text to prevent overlap
                fontsize=9,
                color='black', # Text color can be black for readability against white box
                bbox=dict(boxstyle="round,pad=0.3", fc="white", ec=arrow_color, lw=0.5, alpha=0.9))

# Adjust the y-axis limits to accommodate the annotations below
ax.set_ylim(bottom=base_annotation_y - annotation_y_step * 2) # Extend y-axis further down

plt.tight_layout() # Adjust layout to prevent labels/annotations from being cut off
plt.show()

