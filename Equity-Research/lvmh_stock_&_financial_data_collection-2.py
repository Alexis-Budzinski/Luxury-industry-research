# Convert 'Year' to 'Date' in financial_metrics
financial_metrics['Date'] = pd.to_datetime(financial_metrics['Year'].astype(int), format='%Y')

# Function to align financial metrics with the nearest trading day
def align_financial_metrics_with_trading_days(financial_metrics, stock_returns):
    aligned_data = []

    for _, row in financial_metrics.iterrows():
        year = row['Date'].year
        # Find the nearest trading day in the stock returns data
        trading_days_in_year = stock_returns.loc[str(year)].index
        if not trading_days_in_year.empty:
            # Find the nearest trading day to the start of the year
            nearest_trading_day = min(trading_days_in_year, key=lambda x: abs(x - row['Date']))
            aligned_row = row.to_dict()
            aligned_row['Date'] = nearest_trading_day
            aligned_data.append(aligned_row)

    return pd.DataFrame(aligned_data)

# Align financial metrics with the nearest trading day
aligned_financial_metrics = align_financial_metrics_with_trading_days(financial_metrics, stock_returns_df)

# Calculate Beta for each company
def calculate_beta(stock_returns, market_returns):
    covariance = np.cov(stock_returns, market_returns)[0, 1]
    variance = np.var(market_returns)
    return covariance / variance

# Assuming CAC 40 (^FCHI) is used as the market index
market_returns = stock_returns_df['^FCHI'].dropna()
lvmh_returns = stock_returns_df['MC.PA'].dropna()
kering_returns = stock_returns_df['KER.PA'].dropna()

beta_lvmh = calculate_beta(lvmh_returns, market_returns)
beta_kering = calculate_beta(kering_returns, market_returns)

# Add Beta to the aligned financial metrics
aligned_financial_metrics.loc[aligned_financial_metrics['Company'] == 'LVMH', 'Beta'] = beta_lvmh
aligned_financial_metrics.loc[aligned_financial_metrics['Company'] == 'Kering', 'Beta'] = beta_kering

# Merge aligned financial metrics with stock returns
merged_data = pd.merge(aligned_financial_metrics, stock_returns_df, left_on='Date', right_index=True, how='left')

# Align geopolitical events with the nearest trading day in financial metrics
def align_geopolitical_events_with_trading_days(geopolitical_events, stock_returns):
    aligned_events = []

    for _, row in geopolitical_events.iterrows():
        event_date = row['Date']
        # Explicitly check for NaT and skip if found
        if pd.isna(event_date):
            continue

        year = event_date.year
        # Find the nearest trading day in the stock returns data
        # We can directly use stock_returns.index to check for year presence
        # and then filter, but for precise nearest day, iterating through the year's index is better.
        # Ensure the year is in the index of stock_returns_df
        year_str = str(year)
        if year_str in stock_returns.index.year.astype(str).unique(): # Check if the year exists in stock_returns
            trading_days_in_year = stock_returns.loc[year_str].index
            if not trading_days_in_year.empty:
                # Find the nearest trading day to the event date
                nearest_trading_day = min(trading_days_in_year, key=lambda x: abs(x - event_date))
                aligned_row = row.to_dict()
                aligned_row['Date'] = nearest_trading_day
                aligned_events.append(aligned_row)

    return pd.DataFrame(aligned_events)

# Align geopolitical events with the nearest trading day
aligned_geopolitical_events = align_geopolitical_events_with_trading_days(geopolitical_events_df, stock_returns_df)

# Merge with aligned geopolitical events
merged_data = pd.merge(merged_data, aligned_geopolitical_events, on='Date', how='left', suffixes=('', '_event'))

