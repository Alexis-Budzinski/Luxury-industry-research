covariance_kering = np.cov(returns["KER.PA"], returns["^FCHI"])[0, 1]
beta_kering = covariance_kering / variance_cac40

# Step 3: Fetch additional financial metrics for LVMH and Kering
lvmh = yf.Ticker("MC.PA")
kering = yf.Ticker("KER.PA")

# Fetch P/E ratio, EV/EBITDA, and other metrics
